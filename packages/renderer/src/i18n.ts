export type TranslationKey = keyof typeof dictionaries['en-US'];

export function normalizeLocale(value: string | null | undefined): Locale {
  return value === 'zh-CN' ? 'zh-CN' : 'en-US';
}

export function translate(locale: Locale, key: TranslationKey, params?: Record<string, string | number>): string {
  const template = String(dictionaries[locale][key] ?? dictionaries[DEFAULT_LOCALE][key] ?? key);

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((message, [paramKey, value]) => {
    return message.split(`{${paramKey}}`).join(String(value));
  }, template);
}
